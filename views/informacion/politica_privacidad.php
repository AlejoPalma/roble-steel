    </p>
    <p class="ps-3">
        • Robles Steel SPA, en caso de ser requerido judicialmente al efecto, procederá a comunicar los datos personales
        de los usuarios que le sean solicitados.
    </p>
    <p class="ps-3">
        • Robles Steel SPA podrá comunicar a terceros información estadística elaborada a partir de los datos personales de sus usuarios,
